export const NewPokemon = (
  id: number,
  form: number,
  withRank?: boolean
): Pokemon => {
  const name = PokeList[id - 1];
  const types = getPokeTypes(id, form);
  const base = getPokeBases(id, form);

  const rank =
    withRank &&
    pokeRank.findIndex((poke) => poke.id === id && poke.form === form) + 1
      ? pokeRank.findIndex((poke) => poke.id === id && poke.form === form) + 1
      : undefined;

  const pokemon: Pokemon = {
    id,
    form,
    name,
    types,
    base,
    rank,
  };
  return pokemon;
};
